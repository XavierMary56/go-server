// Project key management for the admin web UI.
function buildProjectKeyDraft(projectId) {
  const slug = (projectId || '')
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '') || 'project';
  const rand = Array.from(crypto.getRandomValues(new Uint8Array(8)))
    .map(n => n.toString(16).padStart(2, '0'))
    .join('');
  return `sk-proj-${slug}-${rand}`;
}

function syncProjectKeyDraft(force = false) {
  const keyInput = document.getElementById('new-proj-key');
  if (!keyInput) return;

  const shouldReplace = force || keyInput.dataset.autoGenerated === '1' || !keyInput.value.trim();
  if (!shouldReplace) return;

  keyInput.value = buildProjectKeyDraft(document.getElementById('new-proj-id').value);
  keyInput.dataset.autoGenerated = '1';
}

function regenerateProjectKey() {
  syncProjectKeyDraft(true);
}

function setProjectKeyModalMode(mode, info) {
  activeProjectKey = info || null;
  const isEdit = mode === 'edit';
  document.getElementById('project-key-modal-title').textContent = isEdit ? '编辑项目密钥' : '查看项目密钥';
  document.getElementById('project-key-modal-desc').textContent = isEdit ? '修改后会立即同步到后台配置。' : '这里显示当前项目密钥的完整信息。';
  document.getElementById('project-key-project-id').value = info && info.project_id || '';
  document.getElementById('project-key-value').value = info && info.key || '';
  document.getElementById('project-key-rate-limit').value = info && info.rate_limit !== undefined ? info.rate_limit : '';
  document.getElementById('project-key-status').value = info && info.enabled ? '已启用' : '已停用';
  document.getElementById('project-key-created-at').value = formatDate(info && info.created_at);
  document.getElementById('project-key-updated-at').value = formatDate(info && info.updated_at);
  document.getElementById('project-key-project-id').readOnly = !isEdit;
  document.getElementById('project-key-value').readOnly = !isEdit;
  document.getElementById('project-key-rate-limit').readOnly = !isEdit;
  document.getElementById('project-key-save-btn').style.display = isEdit ? 'inline-flex' : 'none';
  document.getElementById('project-key-modal').classList.add('show');
}

function closeProjectKeyModal() {
  activeProjectKey = null;
  closeModal('project-key-modal');
}

async function saveProjectKeyModal() {
  if (!activeProjectKey || !activeProjectKey.key) return;

  const projectId = document.getElementById('project-key-project-id').value.trim();
  const key = document.getElementById('project-key-value').value.trim();
  const rateLimitRaw = document.getElementById('project-key-rate-limit').value.trim();
  const rateLimit = parseInt(rateLimitRaw, 10);
  if (!projectId || !key || rateLimitRaw === '' || Number.isNaN(rateLimit) || rateLimit < 0) {
    toast('输入不合法', 'error');
    return;
  }

  const saveBtn = document.getElementById('project-key-save-btn');
  const originalBtnText = saveBtn.textContent;
  saveBtn.disabled = true;
  saveBtn.textContent = '保存中...';

  const resp = await api('PUT', '/v1/admin/keys/' + encodeURIComponent(activeProjectKey.key), {
    project_id: projectId,
    key,
    rate_limit: rateLimit,
    enabled: !!activeProjectKey.enabled
  });
  
  saveBtn.disabled = false;
  saveBtn.textContent = originalBtnText;
  
  if (resp && resp.ok) {
    toast('✓ 项目密钥已更新');
    closeProjectKeyModal();
    loadProjectKeys();
    return;
  }

  const json = await resp.json();
  const errorMsg = json.error || json.message || '更新失败';
  toast('✗ ' + errorMsg, 'error');
}

function renderProjectKeys() {
  const keyword = (document.getElementById('keys-search') && document.getElementById('keys-search').value || '').trim().toLowerCase();
  const tbody = document.getElementById('keys-tbody');
  const rows = projectKeysData.filter(function (info) {
    if (!keyword) return true;
    return `${info.project_id || ''} ${info.key || ''}`.toLowerCase().includes(keyword);
  });

  document.getElementById('keys-count').textContent = `共 ${rows.length} / ${projectKeysData.length} 个项目密钥`;
  if (!rows.length) {
    tbody.innerHTML = '<tr class="empty-row"><td colspan="6">没有匹配的项目密钥</td></tr>';
    return;
  }

  tbody.innerHTML = rows.map(function (info) {
    const encodedKey = encodeURIComponent(info.key || '');
    return `
    <tr>
      <td><strong>${info.project_id || '-'}</strong></td>
      <td><span class="key-cell">${info.key || '-'}</span></td>
      <td>${info.rate_limit ? info.rate_limit + ' 次/分钟' : '不限速'}</td>
      <td>${formatDate(info.created_at)}</td>
      <td><span class="badge ${info.enabled ? 'badge-active' : 'badge-inactive'}">${info.enabled ? '已启用' : '已停用'}</span></td>
      <td><div class="actions">
        <button class="btn btn-sm btn-info" onclick="viewProjectKey('${encodedKey}')">查看</button>
        <button class="btn btn-sm btn-primary" onclick="editProjectKey('${encodedKey}')">编辑</button>
        <button class="btn btn-sm ${info.enabled ? 'btn-warning' : 'btn-success'}" onclick="toggleProjectKey('${encodedKey}', ${!info.enabled})">${info.enabled ? '停用' : '启用'}</button>
        <button class="btn btn-sm btn-danger" onclick="confirmDelete('/v1/admin/keys/${encodedKey}', '项目 ${info.project_id || '-'} 的密钥', loadProjectKeys)">删除</button>
      </div></td>
    </tr>`;
  }).join('');
}

async function loadProjectKeys() {
  const resp = await api('GET', '/v1/admin/keys');
  if (!resp) return;
  const json = await resp.json();
  projectKeysData = Object.values(json.data || {});
  renderProjectKeys();
  syncProjectKeyDraft();
}

async function addProjectKey() {
  const pid = document.getElementById('new-proj-id').value.trim();
  const key = document.getElementById('new-proj-key').value.trim();
  const rate = parseInt(document.getElementById('new-proj-rate').value, 10) || 0;
  if (!pid) {
    toast('项目 ID 不能为空', 'error');
    return;
  }
  if (rate < 0) {
    toast('限速必须大于等于 0', 'error');
    return;
  }

  const submitBtn = document.getElementById('add-keys-submit');
  const originalBtnText = submitBtn.textContent;
  submitBtn.disabled = true;
  submitBtn.textContent = '添加中...';

  const resp = await api('POST', '/v1/admin/keys', {
    project_id: pid,
    key,
    rate_limit: rate
  });
  
  submitBtn.disabled = false;
  submitBtn.textContent = originalBtnText;
  
  if (!resp) {
    toast('请求失败，请检查服务状态', 'error');
    return;
  }

  if (resp.status === 201) {
    toast('✓ 项目密钥添加成功！', 'success');
    document.getElementById('new-proj-id').value = '';
    document.getElementById('new-proj-key').value = '';
    document.getElementById('new-proj-key').dataset.autoGenerated = '1';
    document.getElementById('new-proj-rate').value = '';
    document.getElementById('add-keys-form').classList.remove('show');
    // 稍微延迟重新加载，确保用户看到成功提示
    setTimeout(() => loadProjectKeys(), 500);
    return;
  }

  const json = await resp.json();
  const errorMsg = json.error || json.message || '添加失败，请重试';
  toast('✗ ' + errorMsg, 'error');
}

async function viewProjectKey(enc) {
  const resp = await api('GET', '/v1/admin/keys/' + enc);
  if (!resp || !resp.ok) {
    toast('查询失败', 'error');
    return;
  }
  const json = await resp.json();
  setProjectKeyModalMode('view', json.data || {});
}

async function editProjectKey(enc) {
  const resp = await api('GET', '/v1/admin/keys/' + enc);
  if (!resp || !resp.ok) {
    toast('读取项目密钥失败', 'error');
    return;
  }
  const json = await resp.json();
  setProjectKeyModalMode('edit', json.data || {});
}

async function toggleProjectKey(enc, enable) {
  const resp = await api('PUT', '/v1/admin/keys/' + enc, { enabled: enable });
  if (resp && resp.ok) {
    toast(enable ? '✓ 项目密钥已启用' : '✓ 项目密钥已停用');
    loadProjectKeys();
    return;
  }
  if (!resp) {
    toast('请求失败，请检查服务状态', 'error');
    return;
  }
  const json = await resp.json();
  const errorMsg = json.error || json.message || '操作失败';
  toast('✗ ' + errorMsg, 'error');
}

document.addEventListener('DOMContentLoaded', function () {
  const keyInput = document.getElementById('new-proj-key');
  if (!keyInput) return;

  keyInput.addEventListener('input', function () {
    keyInput.dataset.autoGenerated = keyInput.value.trim() ? '0' : '1';
  });
});
